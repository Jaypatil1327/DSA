#include<iostream>
using namespace std ; 

int main(){
    int x = 1 ;
    int y = 6 ; 
// mathamatical op 
    cout << x+y << endl ;
    cout << x-y << endl ;
    cout << x/y << endl ;
    cout << x%y << endl ;  
// increment & deccrement 
    cout << x++ << endl ; 
    cout << ++x << endl ; 
    
    cout << --x << endl ; 
    cout << x++ << endl ; 

return 0 ; 
}